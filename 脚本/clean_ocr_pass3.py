#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""第三轮：插入缺失讲标题、清理标题残骸、个别错位修复。"""

import re
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parent.parent
DST = PROJECT_ROOT / '整理后文本' / '柏浪涛刑法书_整理版.md'

text = DST.read_text(encoding="utf-8")

# 1. 删除整理稿里残留的讲标题"乱码占位"
JUNK_TITLES = [
    "OZ^7^?^1^\\^要件三：因果关系",
    "/ ）罪数",
    "*/ 1/*经济犯罪",
    "///社会秩序犯罪",
    "?『刑罚的执行和消灭",
    "?『刑罚的执行",
]

for jt in JUNK_TITLES:
    text = text.replace(jt + "\n", "")

# 2. 修复错误识别的"## 第十六讲 ..."（引入的乱七八糟尾巴）
text = re.sub(
    r"## 第十六讲 \"刑罚的执行和消灭\"中\"减刑\"相关内容（第78条）。\s*",
    "",
    text
)

# 3. 用关键词锚点插入讲标题
# 利用每讲的"特别提示"、"第一节"或某些独特内容作锚点
chapters = [
    # (锚点正则, 待插入的标题, 在锚点之前 N 行插入；负值表示当前行替换)
    ("客观要件一：行为", "## 第五讲 客观要件一：行为"),
    ("客观要件二：结果", "## 第六讲 客观要件二：结果"),
    ("客观要件三：因果关系", "## 第七讲 客观要件三：因果关系"),
    # 主观要件 标题：单独一行没法用 grep 简单替换，找前后特征
    # 第八讲入口在 1525 / 2118（第一节犯罪故意）— 用第一节犯罪故意定位
    ("### 第一节 犯罪故意", "## 第八讲 主观要件\n\n### 第一节 犯罪故意"),
    # 第九讲（排除违法事由）—— 第一节正当防卫
    ("### 第一节 正当防卫",
     "## 第九讲 排除犯罪事由Ⅰ：排除违法事由\n\n### 第一节 正当防卫"),
    # 第十讲（排除责任事由）—— 第一节责任年龄
    ("### 第一节 责任年龄",
     "## 第十讲 排除犯罪事由Ⅱ：排除责任事由\n\n### 第一节 责任年龄"),
    # 第十一讲（犯罪形态）—— 第一节犯罪预备
    ("### 第一节 犯罪预备",
     "## 第十一讲 犯罪形态\n\n### 第一节 犯罪预备"),
    # 第十二讲（共同犯罪）—— 第一节共同犯罪的分类与原理
    ("### 第一节 共同犯罪的分类与原理",
     "## 第十二讲 共同犯罪\n\n### 第一节 共同犯罪的分类与原理"),
    # 第十三讲（罪数）—— 第一节一个行为
    ("### 第一节 一个行为",
     "## 第十三讲 罪数\n\n### 第一节 一个行为"),
    # 第十四讲（刑罚的体系）—— 第一节主刑
    ("### 第一节 主刑",
     "## 第十四讲 刑罚的体系\n\n### 第一节 主刑"),
    # 第十五讲（刑罚的裁量）—— 第一节量刑情节
    ("### 第一节 量刑情节",
     "## 第十五讲 刑罚的裁量\n\n### 第一节 量刑情节"),
    # 第十六讲（刑罚的执行和消灭）—— 第一节减刑
    ("### 第一节 减刑",
     "## 第十六讲 刑罚的执行和消灭\n\n### 第一节 减刑"),
    # 第十七讲（分论概说）—— 第一节罪名与罪状
    ("### 第一节 罪名与罪状",
     "## 第十七讲 分论概说\n\n### 第一节 罪名与罪状"),
    # 第十八讲（人身犯罪）—— 第一节侵犯生命、身体的犯罪
    ("### 第一节 侵犯生命、身体的犯罪",
     "## 第十八讲 人身犯罪\n\n### 第一节 侵犯生命、身体的犯罪"),
    # 第十九讲（财产犯罪）—— 第一节基本原理
    ("### 第一节 基本原理",
     "## 第十九讲 财产犯罪\n\n### 第一节 基本原理"),
    # 第二十讲（危害公共安全罪）—— 第一节危险方法型犯罪
    ("### 第一节 危险方法型犯罪",
     "## 第二十讲 危害公共安全罪\n\n### 第一节 危险方法型犯罪"),
    # 第二十一讲（经济犯罪）—— 第一节生产、销售伪劣商品罪
    ("### 第一节 生产、销售伪劣商品罪",
     "## 第二十一讲 经济犯罪\n\n### 第一节 生产、销售伪劣商品罪"),
    # 第二十二讲（社会秩序犯罪）—— 第一节扰乱公共秩序罪
    ("### 第一节 扰乱公共秩序罪",
     "## 第二十二讲 社会秩序犯罪\n\n### 第一节 扰乱公共秩序罪"),
    # 第二十三讲（贪污贿赂罪）—— 第一节贪污罪
    ("### 第一节 贪污罪",
     "## 第二十三讲 贪污贿赂罪\n\n### 第一节 贪污罪"),
    # 第二十四讲（渎职罪）—— 第一节重点罪名
    ("### 第一节 重点罪名",
     "## 第二十四讲 渎职罪\n\n### 第一节 重点罪名"),
]

# 直接替换：第一次出现的 "### 第一节 xxx" → "## 第N讲\n\n### 第一节 xxx"
for anchor, replacement in chapters:
    if anchor.startswith("###"):
        idx = text.find(anchor)
        if idx == -1:
            print(f"!! 未找到锚点：{anchor}")
            continue
        # 检查前面是否已经有该讲的 ## 标题（避免重复插入）
        # 简单起见：如果替换字符串里的 "## 第N讲" 已经出现在 text 中且位置在 idx 之前，则跳过
        chap_title = replacement.split("\n\n")[0]
        if chap_title in text[:idx]:
            continue
        text = text[:idx] + replacement + text[idx + len(anchor):]
    else:
        # 锚点是文本片段（如 "客观要件一：行为"），把孤立的这一行替换成讲标题
        # 仅替换形如 \n锚点\n 的孤立行
        pat = re.compile(r"\n+\s*" + re.escape(anchor) + r"\s*\n+")
        m = pat.search(text)
        if m:
            text = text[:m.start()] + f"\n\n{replacement}\n\n" + text[m.end():]
        else:
            print(f"!! 未找到孤立行锚点：{anchor}")

# 第二十五讲（危害国家安全罪）—— 单独处理
# 整理稿 16935 行 "危害国家安全罪"
pat25 = re.compile(r"\n+\s*危害国家安全罪\s*\n+")
m = pat25.search(text)
if m and "## 第二十五讲" not in text[:m.start()]:
    text = (text[:m.start()] +
            "\n\n## 第二十五讲 危害国家安全罪\n\n" +
            text[m.end():])

# 4. 修正：很多 "## 第N讲 xxx" 后面紧跟"特别提示"应规范化
text = re.sub(
    r"(## 第[一二三四五六七八九十百零\d]+讲 [^\n]+)\n\n特别提示\n",
    r"\1\n\n【特别提示】\n",
    text
)

# 5. 再次收敛空行
text = re.sub(r"\n{3,}", "\n\n", text)

DST.write_text(text, encoding="utf-8")
print("第三轮补丁完成")
print(f"文件总行数：{text.count(chr(10))}")
